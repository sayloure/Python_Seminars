# Считываем количество дней
N = int(input())

# Инициализируем переменные для отслеживания текущей оттепели и максимальной оттепели
current_warm_streak = 0
max_warm_streak = 0

# Проходим по дням и проверяем каждую температуру
for _ in range(N):
    temperature = int(input())
    if temperature > 0:
        current_warm_streak += 1
    else:
        # Если температура стала меньше или равна 0, завершаем текущую оттепель
        if current_warm_streak > max_warm_streak:
            max_warm_streak = current_warm_streak
        current_warm_streak = 0

# Проверяем, если последний период оттепели был самым длинным
if current_warm_streak > max_warm_streak:
    max_warm_streak = current_warm_streak

print(max_warm_streak)


